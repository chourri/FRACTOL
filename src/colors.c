#include "fractol.h"

int	get_trgb(int iters)
{
	int	r;
	int	g;
	int	b;

	r = 1;
	g = 2;
	b = 5;
	return (0 << 24 | r * iters << 19 | g * iters << 8 | b * iters);
}
